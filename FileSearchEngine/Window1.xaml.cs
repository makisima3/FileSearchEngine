        }

        private void TextBox_KeyDown(object sender, KeyEventArgs e)
        {
            if (e.Key == Key.Enter)
            {
                if (!string.IsNullOrWhiteSpace(FileNametb.Text))
                {
                    File.WriteAllText("FileName.txt", FileNametb.Text);
                }
            }
        }
